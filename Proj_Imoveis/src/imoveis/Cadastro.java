    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String mat = jTextField2.getText();
        String tip = jTextField3.getText();
        String log = jTextField4.getText();
        String cid = jTextField5.getText();
        String uf = jTextField6.getText();
        String id = jTextField1.getText();
        
        try {
            if (mat.equals("") || tip.equals("") || log.equals("") || cid.equals("") || uf.equals("")) {
                throw new Exception("Preencha todos os campos");
            }
                 
            int id2 = Integer.parseInt(id);
            String sql = "UPDATE imovel SET matricula = ?, tipo = ?, logradouro = ?, cidade = ?, uf = ? WHERE id = ?";
            prepared = connection.prepareStatement(sql);
            prepared.setString(1, mat);
            prepared.setString(2, tip);
            prepared.setString(3, log);
            prepared.setString(4, cid);
            prepared.setString(5, uf);
            prepared.setInt(6, id2);
            prepared.execute();

            limpaCampos();
            popularTabela();
            jButton3.setEnabled(false);
            jButton4.setEnabled(false);
            jButton2.setEnabled(true);
            JOptionPane.showMessageDialog(this, "Produto alterado com sucesso!", "SUCESSO!", JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage(),
                    "ERRO!",
                    JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (JOptionPane.showConfirmDialog(this, "Tem certeza?") == JOptionPane.OK_OPTION) {
            try {
                statement.executeUpdate("DELETE FROM imovel WHERE id=" + jTextField1.getText());
                limpaCampos();
                popularTabela();
                jButton3.setEnabled(false);
                jButton4.setEnabled(false);
                jButton2.setEnabled(true);

                JOptionPane.showMessageDialog(this, "Imovel removido com sucesso.", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(
                        this,
                        e.getMessage(),
                        "ERRO!",
                        JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(
                    this,
                    "Operação cancelada!",
                    "CANCELADO!",
                    JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_jButton4ActionPerformed

